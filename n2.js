var readlineSync=require('readline-sync');
var num=readlineSync.question('Enter the number');
switch(num)
{
    case "1":
        console.log('one');
    break;
    case "2":
        console.log('Two');
    break;
    case "3":
        console.log('Three');
    break;
    case "4":
        console.log('Four');
    break;
    case "5":
        console.log('Five');
    break;
    case "6":
        console.log('Six');
    break;

}